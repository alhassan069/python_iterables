my_dictionary = {
    "brand": "Ford",
    "model": "Mustang",
    "year": 1964,
    "colors": ["red", "blue"]
}

# Accessing a value
print(my_dictionary["model"])

# Getting all keys
print(my_dictionary.keys())

# Getting all values
print(my_dictionary.values())

# Getting all items
print(my_dictionary.items())